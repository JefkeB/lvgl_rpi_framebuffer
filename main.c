int main(int argc, char **argv)
{
  (void)argc; /*Unused*/
  (void)argv; /*Unused*/

  /*Initialize LVGL*/
  lv_init();

  /*Initialize the HAL (display, input devices, tick) for LVGL*/
  hal_init();


  App();

  while(1) {
      // Periodically call the lv_task handler.	  
      lv_task_handler();
	  
      usleep(5 * 1000);
  }

  return 0;
}

